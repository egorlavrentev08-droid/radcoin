def random_message(message_list):
    """Возвращает случайное сообщение из списка"""
    return random.choice(message_list)


def get_enemy_type(is_mutant, enemy_name):
    """Определяет тип врага для выбора сообщений"""
    if is_mutant:
        return 'мутант'
    else:
        return 'люди'


def get_battle_message(step, is_mutant, medkit_index=None, is_success=None):
    """
    Получить сообщение для определённого шага боя
    
    step: 0-9 (номера шагов)
    is_mutant: True/False (мутант или люди)
    medkit_index: 0/1/2 (какая по счёту аптечка используется)
    is_success: True/False (помогла ли аптечка)
    """
    
    if step == 0:
        return random_message(RETREAT_AND_USE_MEDKIT['мутант' if is_mutant else 'люди'])
    elif step == 1:
        if is_success:
            return random_message(MEDKIT_EFFECT['помогает'])
        else:
            return random_message(MEDKIT_EFFECT['не_помогает'])
    elif step == 2:
        return random_message(BATTLE_CONTINUES['мутант' if is_mutant else 'люди'])
    elif step == 3:
        if medkit_index == 0:
            return random_message(USE_NEXT_MEDKIT['вторую'])
        else:
            return random_message(USE_NEXT_MEDKIT['третью'])
    elif step == 4:
        return random_message(LAST_HOPE_MEDKIT)
    elif step == 5:
        if is_success:
            return random_message(MEDKIT_EFFECT['помогает'])
        else:
            return random_message(MEDKIT_EFFECT['не_помогает'])
    elif step == 6:
        return random_message(BATTLE_CONTINUES['мутант' if is_mutant else 'люди'])
    elif step == 7:
        if is_success:
            return random_message(VICTORY_MESSAGES['мутант' if is_mutant else 'люди'])
        else:
            return random_message(DEATH_MESSAGES['мутант' if is_mutant else 'люди'])
    
    return "⚔️ Бой продолжается..."
